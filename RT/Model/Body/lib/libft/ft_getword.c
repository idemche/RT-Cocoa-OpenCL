#include "libft.h"

char	*ft_getword(const char *s, char c, int *i)
{
	char	*str;
	int		k;

	if (!(str = (char*)malloc(sizeof(str) * (ft_strlen((char*)s)))))
		return (NULL);
	k = 0;
	while (s[*i] != c && s[*i])
	{
		str[k] = s[*i];
		k++;
		*i += 1;
	}
	str[k] = '\0';
	while (s[*i] == c && s[*i])
		*i += 1;
	return (str);
}
