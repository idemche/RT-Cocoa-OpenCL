#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	char *s1_ptr;

	s1_ptr = s1;
	while (n--)
	{
		if (*s2)
			*s1++ = *s2++;
		else
			*s1++ = '\0';
	}
	return (s1_ptr);
}
