#include "rt.h"

void			normalise_quat(t_quaternion *q)
{
	cl_float len;

	len = 1 / sqrtf(q->w * q->w + dot_3(q->v, q->v));
	q->w *= len;
	q->v = mult_3(q->v, len);
}
